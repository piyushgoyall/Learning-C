#include<stdio.h>
int main()
{
	int n,i,j,count=0;
	printf("Enter number : ");
	scanf("%d",&n);
	printf("\n");
	for(i=1;i<=(2*n-1);i++)
	{
		count=n;
		if(i<=n)
	    {
			for(j=1;j<=(2*n-1);j++)
			{
				if(j<=n)
				{
					if(i>j)
					{
						printf("%d",count);
						count=count-1;	
					}
					else
					{
						printf("%d",count);
					}
				}
				else
				{
					if(i+j>=2*n)
					{
						printf("%d",count);	
						count=count+1;
					}
					else
					{
						printf("%d",count);
					}
				}
			}
		}
		if(i>n)
		{
			for(j=1;j<=(2*n-1);j++)
			{
				printf("+");
			}
		}
		
		printf("\n");
	}
}
